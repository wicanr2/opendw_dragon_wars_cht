/* 0x1053 (3D viewport flow sets this to 0x50 via init_offsets(0x50)). */
unsigned short word_1053 = 0;
/* word_1055 is computed inside decode_viewport_data.                  */
static unsigned short word_1055 = 0;
/* 0x104E quadrant dispatch flag.                                      */
unsigned char byte_104E = 0;

/* offsets[] (0xB042). NUM_OFFSETS = 0x88. offsets[i] = i * word_1053. */
#define NUM_OFFSETS 0x88
uint16_t offsets[NUM_OFFSETS];

/* init_offsets() copied from src/lib/offsets.c */
static void init_offsets(unsigned short dx)
{
  int i;
  uint16_t val = 0;
  word_1053 = dx;
  for (i = 0; i < NUM_OFFSETS; i++) {
    offsets[i] = val;
    val += dx;
  }
}

/* get_offset() copied from src/lib/offsets.c */
static uint16_t get_offset(int pos)
{
  return offsets[pos];
}

