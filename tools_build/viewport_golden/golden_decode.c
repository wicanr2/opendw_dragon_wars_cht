/*
 * golden_decode.c
 *
 * Self-contained golden oracle for Dragon Wars viewport decoding.
 *
 * Reproduces the *real* decode_viewport_data logic from Devin Smith's opendw
 * disassembly (src/lib/ui.c) verbatim, plus the run-length nibble-blit
 * dispatch functions (process_quadrant / draw_viewport_word_mode /
 * draw_viewport_neg_x / draw_viewport_neg_x_alt / draw_viewport_flip_y) and
 * the lookup tables (and_table / or_table / b152_table / and_table_B452 /
 * or_table_B652) from src/lib/tables.c and the offsets logic from
 * src/lib/offsets.c.
 *
 * The decode functions are copied byte-for-byte from opendw and NOT modified.
 * Everything VGA/UI/com-related is stubbed away. The only departure from the
 * original is that decode_viewport_data here passes the harness-owned global
 * `viewport_memory` exactly as the original ui.c does (it is a file-static
 * there; here it is a file-global in this single translation unit).
 *
 * Usage:
 *   golden_decode <tmpl_buf.bin> [out.vpmem] [xpos] [ypos] [byte_104E] [word_1053] [fill]
 *
 * Defaults: out = <tmpl_buf>.vpmem, xpos=0, ypos=0, byte_104E=0,
 *           word_1053=0x50 (3D-viewport flow uses init_offsets(0x50)), fill=0x00.
 *
 * The diagnostic printf()s that exist in the original opendw decode functions
 * are preserved (they go to stdout) so behaviour is identical; the actual
 * golden bytes are written to the .vpmem file.
 */

