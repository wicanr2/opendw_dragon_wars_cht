/* D88 */
static void process_quadrant(const struct viewport_data *d, unsigned char *data)
{
  int newx, sign, ax;
  int word_104A;
  uint16_t offset;

  // sar 36C0, 1
  sign = d->xpos & 0x8000;
  newx = d->xpos >> 1;
  newx |= sign;

  ax = d->runlength;
  word_104A = ax;
  ax += newx;
  ax -= word_1053;
  if (ax > 0) {
    word_104A -= ax;
    if (word_104A <= 0)
      return;
  }

  // DA8
  offset = get_offset(d->ypos);
  offset += newx;
  unsigned char *p = data + offset;
  unsigned char *si = d->data + 4;
  for (int i = 0; i < d->numruns; i++) {
    for (int j = 0; j < word_104A; j++) {
      unsigned char val = *si;
      unsigned char dval = *p;

      dval = dval & get_and_table(val);
      dval = dval | get_or_table(val);

      *p = dval;
      p++;
      si++;
    }
    offset += word_1055;
    p = data + offset;
    si += (d->runlength - word_104A);
  }
}

// 0xDEB
static void draw_viewport_word_mode(const struct viewport_data *d, unsigned char *data)
{
  uint16_t ax, old_ax, newx, cx;
  uint16_t dx = 0;
  uint8_t dl;
  int sign;
  int word_104A;
  uint8_t byte_104C;
  uint16_t offset;

  // sar 36C0, 1
  sign = d->xpos & 0x8000;
  newx = d->xpos >> 1;
  newx |= sign;

  dl = 0;

  ax = d->runlength;
  word_104A = ax;
  ax += newx;
  old_ax = ax;
  ax -= word_1053;
  if (ax <= old_ax) {
    // 0x0E06
    word_104A -= ax;
    if (word_104A >= 0) {
      dl--;
    } else {
      // 0xE6C
      return;
    }
  }
  // 0xE0F
  byte_104C = dl;

  offset = get_offset(d->ypos);
  offset += newx;

  ax = ax & 0xFF;

  // 0xE27
  cx = word_104A;
  // 0xE35
  unsigned char *p = data + offset;
  unsigned char *si = d->data + 4;

  // 1048 = 13 ?
  for (int i = 0; i < d->numruns; i++) {
    for (int j = 0; j < cx; j++) {
      unsigned char val = *si;

      dx = p[0];
      dx += p[1] << 8;

      dx &= get_and_table_B452(val);
      dx |= get_or_table_B652(val);

      *p = dx & 0xFF;
      p++;
      *p = (dx & 0xFF00) >> 8;
      printf("(0x%04X) ", dx);
      si++;
    }
    printf("\n");
    // 0xE4C
    *p = (dx & 0xFF);
    if (byte_104C < 0x80) {
      p++;
      *p = (dx & 0xFF00) >> 8;
    }
    // 0x3A + 0x13
    // offset += 1055
    offset += word_1055;
    p = data + offset;
    si += (d->runlength - word_104A);
  }
}

// 0xEC5
static void draw_viewport_neg_x_alt(const struct viewport_data *d, unsigned char *data)
{
  uint16_t ax;
  int bx;
  int sign, word_104A;
  uint16_t offset, save;
  uint16_t dx;
  unsigned char *ds = d->data + 4;
  unsigned char *base;
  uint8_t al;

  ax = d->xpos;
  ax = -ax;
  // sar ax, 1
  sign = ax & 0x8000;
  ax = ax >> 1;
  ax |= sign;

  bx = d->runlength;
  bx -= ax;
  word_104A = bx;
  if (word_104A <= 0) {
    return;
  }

  // 0xEDB
  ax = ax & 0xFF;
  ds += ax; // add si, ax
  bx = d->ypos;
  offset = get_offset(d->ypos);
  offset--;

  // 0xEEE
  for (int i = 0; i < d->numruns; i++) {
    save = offset;
    base = ds;
    unsigned char *p = data + offset;
    al = *ds++;
    bx = al;
    dx = p[0];
    dx += p[1] << 8;
    dx &= get_and_table_B452(bx);
    dx |= get_or_table_B652(bx);
    p++;
    *p = (dx & 0xFF00) >> 8;

    // 0xF10
    for (int j = 0; j < (word_104A - 1); j++) {
      al = *ds++;
      bx = al;
      dx = p[0];
      dx += p[1] << 8;

      dx &= get_and_table_B452(bx);
      dx |= get_or_table_B652(bx);

      *p = dx & 0xFF;
      p++;
      *p = (dx & 0xFF00) >> 8;
    }
    offset = save;
    offset += word_1055;
    p = data + offset;
    base += d->runlength;
    ds = base;
  }
}

// 0xE6D
static void draw_viewport_neg_x(const struct viewport_data *d, unsigned char *data)
{
  uint16_t ax, cx, dx;
  uint8_t al;
  uint16_t offset;
  uint16_t word_104A;
  int bx, sign;
  int si = 4;


  ax = d->xpos;
  ax = -ax;
  al = ax & 0xFF;

  // sar al, 1
  sign = al & 0x80;
  al = al >> 1;
  al |= sign;
  ax = (ax & 0xFF00) | al;

  bx = d->runlength;
  bx -= ax;

  word_104A = bx;
  if (bx <= 0)
    return;

  // 0xE83
  ax = ax & 0xFF;

  si += ax;
  bx = d->ypos;
  dx = get_offset(bx);
  ax = ax & 0xFF;

  // 0xE95
  for (int i = 0; i < d->numruns; i++) {
    cx = word_104A;
    offset = dx;
    // bp = si
    unsigned char *p = data + offset;
    unsigned char *ds = d->data + si;
    printf("%s: lodsb: 0x%02X\n", __func__, *ds);

    // 0xE9E
    for (int j = 0; j < cx; j++) {
      al = *ds++;
      bx = al;
      al = *p;
      al &= get_and_table(bx);
      al |= get_or_table(bx);
      *p = al;
      p++;
    }
    si += d->runlength;
    dx += word_1055;
  }
}

// 0xF40 (flip y)
static void draw_viewport_flip_y(const struct viewport_data *d, unsigned char *data)
{
  int cf = 0;
  int ax, bx, dx, word_104A;
  int di;
  uint8_t al, bl, bh;
  unsigned char *p = d->data + 4;
  unsigned char *q;

  // SAR xpos, 1
  uint16_t new_x = d->xpos;
  if (new_x & 1) {
    cf = 1;
  }
  new_x = new_x >> 1;

  ax = d->runlength;
  word_104A = ax;
  ax += new_x;
  ax -= word_1053;

  if (ax > 0) {
    // F56
    bx = d->runlength;
    bx -= ax;
    word_104A = bx;
    if (word_104A <= 0) {
      return;
    }
  }
  // F64
  bx = d->ypos;
  dx = new_x;
  dx += get_offset(bx);

  p += d->runlength;
  p--;
  q = p;
  bh = 0;

  // F7D

  for (int i = 0; i < d->numruns; i++) {
    di = dx;
    for (int j = 0; j < word_104A; j++) {
      bl = *q--;

      bl = get_b152_table(bl);

      al = data[di];
      al &= get_and_table(bl);
      al |= get_or_table(bl);

      data[di] = al;
      di++;
    }
    dx += word_1055;
    p += d->runlength;
    q = p;
  }
  (void)cf; (void)bh; (void)ax;
}

// 0xCF8 - extract and process viewport data.
void decode_viewport_data(unsigned char *data, struct viewport_data *vp)
{
  int ax;
  uint8_t al;
  uint16_t bx;
  unsigned char *ds = data;

  vp->runlength = *ds++;
  vp->numruns = *ds++;

  printf("RL: %d, NR: %d\n", vp->runlength, vp->numruns);

  al = *ds++;
  ax = (int8_t)al;
  if (byte_104E & 0x80) {
    // neg ax;
    ax = -ax;
  }
  vp->xpos += ax;
  if (vp->runlength >= 0x80 && byte_104E >= 0x80) {
    vp->xpos--;
  }
  // 0xD2A
  vp->runlength &= 0x7F;
  al = *ds++;
  ax = (int8_t)al;

  if (byte_104E & 0x40) {
    ax = -ax;
  }
  vp->ypos += ax;

  bx = vp->xpos;
  bx &= 1;
  bx = bx << 1;
  if (vp->xpos >= 0x8000) {
    bx |= 4;
  }

  // 0xD52
  if (byte_104E & 0x80) {
    bx |= 8;
  }

  // 0xD5C
  ax = word_1053;
  if (byte_104E & 0x40) {
    ax = -ax;
  }
  // 0xD67
  word_1055 = ax;

  // 0xD78 offset
  switch (bx) {
  case 0:
    process_quadrant(vp, viewport_memory);
    break;
  case 2:
    draw_viewport_word_mode(vp, viewport_memory);
    break;
  case 4:
    draw_viewport_neg_x(vp, viewport_memory);
    break;
  case 6:
    draw_viewport_neg_x_alt(vp, viewport_memory);
    break;
  case 8:
    draw_viewport_flip_y(vp, viewport_memory);
    break;
  default:
    printf("%s: An unhandled BX (0x%04X) was specified.\n", __func__, bx);
    exit(1);
    break;
  }
}

