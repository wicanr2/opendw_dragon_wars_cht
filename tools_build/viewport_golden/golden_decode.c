static long parse_num(const char *s, long dflt)
{
  if (!s) return dflt;
  return (long)strtol(s, NULL, 0);
}

int main(int argc, char **argv)
{
  if (argc < 2) {
    fprintf(stderr,
      "Usage: %s <tmpl_buf.bin> [out.vpmem] [xpos] [ypos] [byte_104E] [word_1053] [fill]\n"
      "  Defaults: out=<tmpl_buf>.vpmem xpos=0 ypos=0 byte_104E=0 word_1053=0x50 fill=0x00\n",
      argv[0]);
    return 2;
  }

  const char *in_path = argv[1];
  char out_path[4096];
  if (argc >= 3 && argv[2][0] != '\0') {
    snprintf(out_path, sizeof(out_path), "%s", argv[2]);
  } else {
    snprintf(out_path, sizeof(out_path), "%s.vpmem", in_path);
  }

  long cli_xpos     = parse_num(argc >= 4 ? argv[3] : NULL, 0);
  long cli_ypos     = parse_num(argc >= 5 ? argv[4] : NULL, 0);
  long cli_104E     = parse_num(argc >= 6 ? argv[5] : NULL, 0);
  long cli_1053     = parse_num(argc >= 7 ? argv[6] : NULL, 0x50);
  long cli_fill     = parse_num(argc >= 8 ? argv[7] : NULL, 0x00);

  /* Load the tmpl_buf. */
  FILE *fin = fopen(in_path, "rb");
  if (!fin) { perror("fopen tmpl_buf"); return 1; }
  fseek(fin, 0, SEEK_END);
  long fsize = ftell(fin);
  fseek(fin, 0, SEEK_SET);
  if (fsize <= 0) { fprintf(stderr, "empty tmpl_buf\n"); fclose(fin); return 1; }
  unsigned char *tmpl_buf = (unsigned char *)malloc((size_t)fsize);
  if (fread(tmpl_buf, 1, (size_t)fsize, fin) != (size_t)fsize) {
    perror("fread"); fclose(fin); free(tmpl_buf); return 1;
  }
  fclose(fin);

  /* 1. allocate viewport_memory and fill. */
  viewport_memory = (unsigned char *)malloc(viewport_mem_sz);
  memset(viewport_memory, (int)cli_fill, viewport_mem_sz);

  /* 2. set globals.
   *    init_offsets(word_1053) reproduces the 3D-viewport flow
   *    (engine.c calls init_offsets(0x50) before the 3D map view;
   *     the minimap flow uses 0x90). offsets[i] = i * word_1053. */
  init_offsets((unsigned short)cli_1053);
  byte_104E = (unsigned char)cli_104E;

  /* 3. set vp. xpos/ypos seeded from CLI (default 0). In the real game
   *    these come from viewport_metadata per quadrant; for a standalone
   *    tmpl_buf decode we make them configurable. */
  struct viewport_data vp;
  memset(&vp, 0, sizeof(vp));
  vp.xpos = (uint16_t)cli_xpos;
  vp.ypos = (int)cli_ypos;
  vp.data = tmpl_buf;

  fprintf(stderr,
    "[golden] in=%s out=%s  xpos=0x%04lX ypos=%ld byte_104E=0x%02lX word_1053=0x%lX fill=0x%02lX tmpl_sz=%ld\n",
    in_path, out_path, cli_xpos, cli_ypos, cli_104E, cli_1053, cli_fill, fsize);

  /* 4. decode. */
  decode_viewport_data(tmpl_buf, &vp);

  /* 5. write viewport_memory. */
  FILE *fout = fopen(out_path, "wb");
  if (!fout) { perror("fopen out"); return 1; }
  if (fwrite(viewport_memory, 1, viewport_mem_sz, fout) != (size_t)viewport_mem_sz) {
    perror("fwrite"); fclose(fout); return 1;
  }
  fclose(fout);

  /* report first 32 bytes */
  printf("\n[golden] post-decode vp: xpos=0x%04X ypos=%d runlength=%d numruns=%d\n",
         vp.xpos, vp.ypos, vp.runlength, vp.numruns);
  printf("[golden] wrote %d bytes to %s\n", viewport_mem_sz, out_path);
  printf("[golden] first 32 bytes of viewport_memory:\n");
  for (int i = 0; i < 32; i++) {
    printf("%02X ", viewport_memory[i]);
    if ((i & 15) == 15) printf("\n");
  }
  printf("\n");

  free(tmpl_buf);
  free(viewport_memory);
  return 0;
}
