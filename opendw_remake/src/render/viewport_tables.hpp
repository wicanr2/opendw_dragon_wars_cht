// ---------------------------------------------------------------------------
// offsets[] —— get_offset(pos) 的查表
//
// 來源:offsets.c:24 (宣告) / offsets.c:31-44 (init_offsets)
// 用途:get_offset(pos) 回傳 offsets[pos];為 viewport 列 stride 的累加查表。
// 元素數:NUM_OFFSETS = 0x88 = 136。
//
// 注意:opendw 並未把 offsets[] 寫成靜態字面表,而是由 init_offsets(dx) 算出:
//   word_1053 = dx;
//   for (i = 0; i < 0x88; i++) { offsets[i] = i * dx; }
// dx (= 列 stride 基準, 見 word_1053 下方) 由呼叫端帶入 0x50 或 0x90。
// 因此這裡不放固定字面表,而提供等價的 constexpr 生成函式,
// 由呼叫端用對應 stride (0x50 / 0x90) 產生。
//
// word_1053 (offsets.c:27 宣告 / offsets.c:37 設定):
//   word_1053 = dx,即傳入 init_offsets 的列 stride 基準。
//   呼叫端 0x17DD 帶 0x50、0x17E2 帶 0x90。
//   它不是常數;它記錄「目前 offsets[] 是用哪個 stride 建的」。
inline constexpr int kNumOffsets = 0x88; // 136

// 等價於 opendw init_offsets(stride):offsets[i] = i * stride。
// stride 應為 0x50 或 0x90。回傳的 word_1053 即 stride。
struct OffsetTable {
  std::uint16_t offsets[kNumOffsets];
  std::uint16_t word_1053; // = stride
};

inline constexpr OffsetTable make_offset_table(std::uint16_t stride) {
  OffsetTable t{};
  std::uint16_t val = 0;
  for (int i = 0; i < kNumOffsets; ++i) {
    t.offsets[i] = val;
    val = static_cast<std::uint16_t>(val + stride);
  }
  t.word_1053 = stride;
  return t;
}

